    static function find_cat($pre, $fac_common)
    {

        foreach ($fac_common as $site => $facs)
        {
            if(strpos($pre, $site) !== false)
            {
                foreach ($facs as $key => $fac)
                {
                    foreach ($fac as $url)
                    {
                        if(strpos($pre, $url) or $pre == $url)
                        {
                            return $key;
                        }
                    }
                }
            }
        }
        if(strpos($pre, 'aktsii') !== false or strpos($pre, 'aktcii') !== false or strpos($pre, 'promotions') !== false or in_array($pre, self::MIRK_PROMOS))
        {
            $key =  "Акции";
        }
        elseif(strpos($pre, 'activities') !== false or strpos($pre, 'meropriyatiya') !== false or in_array($pre, self::MIRK_ACT))
        {
            $key =  "Мероприятия";
        }
        elseif (strpos($pre, 'modelyam') !== false or strpos($pre, 'for-models') !== false or strpos($pre, 'promotions') !== false or strpos($pre, 'modeli') !== false)
        {
            $key =  "Модели";
        }
        elseif (strpos($pre, 'raspisanie') !== false or strpos($pre, 'rasp-mirk') !== false)
        {
            $key =  "Расписание";
        }
        elseif (strpos($pre, 'checkout') !== false or strpos($pre, 'cart') !== false)
        {
            $key =  "Корзина";
        }
        elseif (strpos($pre, 'articles') !== false or strpos($pre, 'cart') !== false)
        {
            $key =  "Статьи";
        }
        elseif(in_array(rtrim($pre, '/'), ['mzpo-s.ru', 'mirk.msk.ru', 'mzpo.education', 'mzpokurs.com']))
        {
            $key =  "Главная страница";
        }
        else
        {
            $key =  "Остальное";
//        dd([$key, $pre], 0);

        }
        return $key;
    }

    public static function divide($a, $b){
        try {
            if(@($a / $b) === false) return INF; // covers PHP5
            return @($a / $b); // covers PHP7
        } catch (DivisionByZeroError $e) {
            return INF; // covers PHP8
        }
    }

